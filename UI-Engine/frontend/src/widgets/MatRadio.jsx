import * as React from "react";
import Radio from "@mui/material/Radio";
import RadioGroup from "@mui/material/RadioGroup";
import FormControlLabel from "@mui/material/FormControlLabel";
import FormControl from "@mui/material/FormControl";

export default function MatRadio({ label, fields, field, ...props }) {
  return (
    <FormControl>
      <RadioGroup
        row
        aria-labelledby="demo-row-radio-buttons-group-label"
        name="row-radio-buttons-group"
        onChange={props.onChange}
        value={field?.defaultVal ? field.defaultVal : ""}
        style={{display:"flex",justifyContent:"space-around",width:"120%"}}
      >
        {fields?.map((field) => {
          return (
            <FormControlLabel
              value={field.value}              
              control={<Radio checked={field.checked}/>}
              label={field.label}
              sx={{ mr: 1, color: "black" }}
            />
          );
        })}
      </RadioGroup>
    </FormControl>
  );
}


